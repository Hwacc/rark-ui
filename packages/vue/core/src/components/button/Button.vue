<script setup lang="ts">
import type { ButtonVariants } from '@rark-ui/themes/default'
import type { ButtonProps } from '.'
import { useForwardExpose } from '@ark-ui/vue'
import { ark } from '@ark-ui/vue/factory'
import { getNodeCssVar } from '@rark-ui/shared/css'
import { useRipple } from '@rark-ui/vue/composables/useRipple'
import { useTheme } from '@rark-ui/vue/composables/useTheme'
import { LoaderCircle } from 'lucide-vue-next'
import { computed } from 'vue'

const {
  variant = 'default',
  class: propsClass,
  theme: propsTheme,
  disabled,
  ripple = false,
  loading = false,
  asChild = false,
  ui,
} = defineProps<ButtonProps>()

const emits = defineEmits<{
  click: [event: MouseEvent]
}>()
const slots = defineSlots<{
  default?: () => any
  tooltip?: () => any
  loading?: () => any
}>()

const { forwardRef, currentElement } = useForwardExpose()
const rippleColor = computed(() => {
  return getNodeCssVar(currentElement.value, '--rui-ripple-color', 'transparent')
})
const { onRipple, Ripple } = useRipple(currentElement, {
  enabled: ripple && !disabled,
  color: rippleColor,
})

function onClick(event: MouseEvent) {
  onRipple(event)
  emits('click', event)
}

// theme
const theme = useTheme(() => propsTheme)
const crafts = computed(() => theme.value.crafts.tvButton())
</script>

<template>
  <ark.button
    :ref="forwardRef"
    :class="crafts.root({
      variant: variant as ButtonVariants['variant'],
      loading,
      class: [ui?.root?.class, propsClass],
      ...theme,
    })"
    :disabled="disabled"
    :data-variant="variant"
    :data-ripple="ripple ? true : undefined"
    :data-loading="loading ? true : undefined"
    :data-theme-size="theme.size"
    :as-child="asChild"
    @click="onClick"
  >
    <slot
      v-if="loading"
      name="loading"
    >
      <LoaderCircle
        :class="crafts.loading({
          variant: variant as ButtonVariants['variant'],
          loading,
          class: [ui?.loading?.class],
          ...theme,
        })"
      />
    </slot>
    <slot />
    <Ripple v-if="ripple" />
  </ark.button>
</template>
