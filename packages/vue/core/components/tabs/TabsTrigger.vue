<script lang="ts">
export interface TabsTriggerProps extends TabTriggerBaseProps, ThemeNoCrafts {
  class?: HTMLAttributes['class']
}
</script>

<script setup lang="ts">
import type { TabTriggerBaseProps } from '@ark-ui/vue/tabs'
import type { ThemeNoCrafts } from '@rui-ark/vue/providers/theme'
import type { HTMLAttributes } from 'vue'
import { useForwardProps } from '@ark-ui/vue'
import { Tabs } from '@ark-ui/vue/tabs'
import { useTheme } from '@rui-ark/vue/composables/useTheme'
import { computed } from 'vue'
import { injectTabsContextEx } from './TabsProviderEx.vue'

const { class: propsClass, theme: propsTheme, ...props } = defineProps<TabsTriggerProps>()
const forwarded = useForwardProps(props)
const contextEx = injectTabsContextEx()

// theme
const theme = useTheme(() => propsTheme)
const crafts = computed(() => theme.value.crafts.tvTabs())
</script>

<template>
  <Tabs.Trigger
    v-bind="forwarded"
    :class="crafts.trigger({ class: [propsClass], orientation: contextEx.orientation, ...theme })"
  >
    <slot />
  </Tabs.Trigger>
</template>
